What are value type avriables ?
A data type is a set of values and the allowable operations on those values.

Kotlin is a strongly statically typed programming language.
Every variable and every expression has a type that is known at compile time.
Kotlin is also a strongly typed language because types limit the values that a variable can hold or
that an expression can produce, limit the operations supported on those values,
and determine the meaning of the operations. Strong static typing helps detect errors at compile time.
Kotlin does not feature implicit conversions between types.

value type variables list
Boolean
Char
Byte
Short
Int
Long
Float
Double
Array
There is also a special null type which represents a non-existing value.

