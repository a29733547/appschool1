3. What is the Android Jetpack?*/

/**Jetpack 是 Android 軟體組件的集合，使您可以更輕鬆地開發出色的 Android 應用。
這些組件可幫助您遵循最佳做法、讓您擺脫編寫樣板代碼的工作並簡化複雜任務，以便您將精力集中放在所需的代碼上。
Jetpack 包含與平台 API 解除捆綁的 androidx.* 軟體包庫。
這意味著，它可以提供向後兼容性，且比 Android 平台的更新頻率更高，以此確保您始終可以獲取最新且最好的 Jetpack 組件版本。

使用JetPack的好處
1.消除大量重複樣板式的代碼。
2.簡化複雜的任務。
3.提供了強健的向後兼容的能力。
4.加速Android的開發進程。

官方給出的優勢
加速開發 組件可以單獨採用（不過這些組件是為協同工作而構建的），同時利用 Kotlin 語言功能幫助您提高工作效率。
消除樣板代碼 Android Jetpack可管理繁瑣的Activity（如後台任務、導航和生命周期管理），以便您可以專注於如何讓您的應用出類拔萃。
構建高質量的強大應用 Android Jetpack 組件圍繞現代化設計實踐構建而成，具有向後兼容性，可以減少崩潰和內存泄漏。

